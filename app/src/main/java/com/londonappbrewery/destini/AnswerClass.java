public class AnswerClass {
    private int mAnsw1;
    private int mAnsw2;

    public AnswerClass(int answ1, int answ2) {
        mAnsw1 = answ1;
        mAnsw2 = answ2;
    }

    public int getAnsw1() {
        return mAnsw1;
    }

    public void setAnsw1(int answ1) {
        mAnsw1 = answ1;
    }

    public int getAnsw2() {
        return mAnsw2;
    }

    public void setAnsw2(int answ2) {
        mAnsw2 = answ2;
    }
}
